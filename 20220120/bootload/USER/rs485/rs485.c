/*
****************************************************************************************
* Function: crc16_list
* Description: crc16校验查表法
* Calls: None
* Called By: None
* Input: *puchmsg    校验数据首地址
*        usdatalen    校验数据个数
* Output: None
* Return: 校验结果
* Author: LeeSong
* Others: None
* date of completion: 2019-02-18
* date of last modify: 2019-02-18
****************************************************************************************
*/
uint16_t crc16_list(uint8_t *puchmsg, uint16_t usdatalen)
{
	uint8_t uch_crc_hi = 0xFF;
	uint8_t uch_crc_lo = 0xFF;
	uint32_t uindex;
	
	while (usdatalen--)
	{
		uindex = uch_crc_hi ^ *puchmsg++;
		uch_crc_hi = uch_crc_lo ^ auch_crc_hi[uindex];
		uch_crc_lo = auch_crc_lo[uindex];
	}
	
	return (uch_crc_hi | uch_crc_lo <<8);
}

